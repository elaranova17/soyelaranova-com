function setPropValLanguage(lang) {
  if (!PROP_VAL_I18N[lang]) return;
  document.documentElement.setAttribute('lang', lang === 'fr' ? 'fr' : 'es');
  document.documentElement.setAttribute('data-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (PROP_VAL_I18N[lang][key] !== undefined) {
      el.innerHTML = PROP_VAL_I18N[lang][key];
    }
  });

  document.querySelectorAll('[data-lang-btn]').forEach((btn) => {
    btn.setAttribute('data-active', btn.getAttribute('data-lang-btn') === lang ? 'true' : 'false');
  });

  const titleKey = 'doc.title';
  if (PROP_VAL_I18N[lang][titleKey]) {
    document.title = PROP_VAL_I18N[lang][titleKey].replace(/<[^>]+>/g, '');
  }

  try {
    localStorage.setItem('prop-val-debarras-lang', lang);
  } catch {
    /* ignore */
  }
}

document.querySelectorAll('[data-lang-btn]').forEach((btn) => {
  btn.addEventListener('click', () => setPropValLanguage(btn.getAttribute('data-lang-btn')));
});

(function initPropValLang() {
  let saved = null;
  try {
    saved = localStorage.getItem('prop-val-debarras-lang');
  } catch {
    /* ignore */
  }
  const params = new URLSearchParams(window.location.search);
  const urlLang = params.get('lang');
  const browser = (navigator.language || 'fr').slice(0, 2).toLowerCase();
  const initial =
    (urlLang && PROP_VAL_I18N[urlLang] ? urlLang : null) ||
    (saved && PROP_VAL_I18N[saved] ? saved : null) ||
    (PROP_VAL_I18N[browser] ? browser : 'fr');
  setPropValLanguage(initial);
})();
